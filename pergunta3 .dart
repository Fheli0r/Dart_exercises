void main() {
  //A) os numeros são iguais ao anterior so que com mais 2
  print('1,3,5,7,9');

  //B) cada numero é igual ao anterior multiplicado por 2
  print('2,4,8,16,32,64,128');

  //C) cada numero é igual ao anterior so que com acrescimo de um numero impar
  print('0, 1, 4, 9, 16, 25, 36,49');

  //D) cada número é igual ao quadrado dos números pares
  print(' 4, 16, 36, 64, 100');

  //E)Essa é a sequencia fibonacci onde o proximo numero é a soma dos dois ultimos
  print('1, 1, 2, 3, 5, 8,13');

  //F)Todos os numeros dessa sequencia possuem a inicial D então o proximo seria o mesmo
  print('2,10, 12, 16, 17, 18, 19, 200');
}
